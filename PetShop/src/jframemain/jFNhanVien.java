    public boolean kiemTraXoaKhachHang(String maKH){
        if(maKH.isEmpty()){
            JOptionPane.showMessageDialog(this, "Mã khách hàng không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(KhachHangDAO.kiemTraMaKhachHang(maKH)==false){
            JOptionPane.showMessageDialog(null, "Khách hàng ["+maKH+"] không tồn tại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(HoaDonDAO.kiemTraMaKhachHang(maKH)){
            JOptionPane.showMessageDialog(this, "Vui lòng xóa hóa đơn "
                    + "của khách hàng có mã ["+maKH+"] trước khi"
                            + " thực hiện lại thao tác này!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    private void btnXacNhan_KH_NVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhan_KH_NVActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");  
        LocalDateTime now = LocalDateTime.now();  
        String maKH = txtMaKH_KH_NV.getText();
        String hoTen = txtHoTen_KH_NV.getText();
        String gioiTinh = cbbGioiTinh_KH_NV.getSelectedItem().toString();
        String sdt = txtSDT_KH_NV.getText();
        String diaChi = txtDiaChi_KH_NV.getText();
        String ngayDK = txtNgayDK_KH_NV.getText();
        if(ngayDK.isEmpty()){
            ngayDK = dtf.format(now);
        }
        KhachHang kh = new KhachHang(maKH, hoTen, gioiTinh, sdt, diaChi, ngayDK);
        if(kiemTraXacNhanKhachHang(maKH, hoTen, cbbGioiTinh_KH_NV, sdt, diaChi, ngayDK)){
            if(KhachHangDAO.kiemTraMaKhachHang(maKH)){
                int op = JOptionPane.showConfirmDialog(null,
                            "Mã khách hàng ["+maKH+"] đã tồn tại, bạn có muốn cập nhật?",
                            "Xác nhận", JOptionPane.YES_NO_OPTION);
                if(op==JOptionPane.YES_OPTION){
                    if(sdt.equals(KhachHangDAO.layThongTinKhachHangTheoMaKH(maKH).getSdt()) == false && KhachHangDAO.kiemTraSDTKhachHang(sdt)){
                        JOptionPane.showMessageDialog(this, "Số điện thoại ["+sdt+"] đang thuộc về một khách hàng khác!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    try{
                        boolean kq = KhachHangDAO.suaKhachHang(kh);
                        if(kq){
                            hienThiDSKH();
                            JOptionPane.showMessageDialog(this, "Cập nhật thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "Lỗi cập nhật!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                        }                
                    }
                    catch(Exception ex){
                        ex.printStackTrace();
                    }
                }
                else return;
            }
            else{
                int op = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn thêm khách hàng "
                            + "["+maKH+"]?", "Xác nhận", JOptionPane.YES_NO_OPTION);
                if(op == JOptionPane.YES_OPTION)
                    if(KhachHangDAO.kiemTraSDTKhachHang(sdt)){
                        JOptionPane.showMessageDialog(this, "Số điện thoại ["+sdt+"] đang thuộc về một khách hàng khác!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    try{
                        boolean kq =KhachHangDAO.themKhachHang(kh);
                        if(kq){
                            hienThiDSKH();
                            JOptionPane.showMessageDialog(this, "Thêm thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE); 
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "Thêm không thành công!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    catch(Exception ex){
                        ex.printStackTrace();
                    }
            }
        }
            
    }//GEN-LAST:event_btnXacNhan_KH_NVActionPerformed

    private boolean  kiemTraXacNhanKhachHang(String maKH, String hoTen, JComboBox gioiTinh, String sdt, String diaChi, String ngayDK){
        if(maKH.isEmpty()){
            JOptionPane.showMessageDialog(this, "Mã khách hàng không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkMaKH(maKH)==false){
            JOptionPane.showMessageDialog(this, "Mã khách hàng tối đa 20 ký tự, theo định dạng KH đứng trước dãy số, không chứa ký tự đặc biệt và khoảng trắng!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(hoTen.isEmpty()){
            JOptionPane.showMessageDialog(this, "Họ tên không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkHoTen(hoTen) == false){
            JOptionPane.showMessageDialog(this, "Họ tên tối đa 50 ký tự, không bao gồm ký tự đặc biệt và số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(gioiTinh.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this, "Giới tính không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(sdt.isEmpty()){
            JOptionPane.showMessageDialog(this, "Số điện thoại không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkSDT(sdt)==false){
            JOptionPane.showMessageDialog(this, "Số điện thoại chỉ bao gồm ký tự số có độ dài 10 hoặc 11 ký tự!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(diaChi.isEmpty()){
            JOptionPane.showMessageDialog(this, "Địa chỉ không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(diaChi.length()>200){
            JOptionPane.showMessageDialog(this, "Địa chỉ không được vượt quá 200 ký tự!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkNgayThang(ngayDK)==false){
            JOptionPane.showMessageDialog(this, "Ngày tháng hoặc định dạng không không hợp lệ, dd-MM-yyyy hoặc dd/MM/yyyy!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
