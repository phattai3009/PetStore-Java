    private void btnThanhToan_BH_NVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToan_BH_NVActionPerformed
        // TODO add your handling code here: 
        String maKH = txtMaKH_BH_NV.getText();
        String tenTK = jFLogin.userToGet;
        String tongTien = txtTongTien_BH_NV.getText();
        String tienNhan = txtTienNhan_BH_NV.getText();
        NhanVien nv = NhanVienDAO.layThongTinNhanVienTheoUser(tenTK);
        if(kiemTraThanhToan(tenTK, maKH, tongTien, tienNhan)){ 
            long longTongTien = Long.valueOf(tongTien);
            long longTienNhan = Long.valueOf(tienNhan);
            long tienThua = longTienNhan - longTongTien;
            txtTienThua_BH_NV.setText(String.valueOf(tienThua));
            HoaDon hd = new HoaDon(nv.getMaNV(), maKH, longTongTien, longTienNhan, tienThua);
            int op = JOptionPane.showConfirmDialog(null, "Bạn có chắc sẽ thanh toán không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if(op == JOptionPane.YES_OPTION){
                boolean kqHD = HoaDonDAO.themHoaDon(hd);
                boolean kqCTHD = false;
                int maHD = Integer.valueOf(HoaDonDAO.layMaHoaDon());
                if(kqHD){
                    ArrayList<GioHang> dsGH = GioHangDAO.layDSItemGioHang();
                    String maTC;
                    long donGia, soLuong, thanhTien;

                    for(GioHang gh: dsGH){
                        maTC = gh.getMaTC();
                        donGia= gh.getDonGia();
                        soLuong = gh.getSoLuong();
                        thanhTien = gh.getThanhTien();
                        CTHoaDon ct = new CTHoaDon(maHD, maTC, soLuong);                  
                        kqCTHD = HoaDonDAO.themChiTietHoaDon(ct);
                    }
                }
                if(kqCTHD){
                    JOptionPane.showMessageDialog(null, "Thanh toán thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    GioHangDAO.xoaGioHang();
                    hienThiGioHang();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Thanh toán không thành công", "Lỗi", JOptionPane.ERROR_MESSAGE);
                 }

            }
        }
    }//GEN-LAST:event_btnThanhToan_BH_NVActionPerformed

    private boolean kiemTraThanhToan(String tenTK, String maKH, String tongTien, String tienNhan){
        if(tenTK.isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa đăng nhập nên không thể thực hiện thao tác này!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(tongTien.isEmpty()){
            JOptionPane.showMessageDialog(null, "Giỏ hàng đang rỗng!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(tienNhan.isEmpty()){
            JOptionPane.showMessageDialog(null, "Tiền nhận không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(maKH.isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn khách hàng!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(tienNhan.isEmpty()){
            JOptionPane.showMessageDialog(this, "Tiền nhận từ khách hàng không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkSoNguyenDuong(tienNhan)==false || Integer.valueOf(tienNhan)==0){
            JOptionPane.showMessageDialog(this, "Tiền nhận từ khách phải là số nguyên lớn hơn 0!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Integer.valueOf(tienNhan) < Integer.valueOf(tongTien)){
            JOptionPane.showMessageDialog(this, "Không đủ tiền để thanh toán!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    private void btnTimKiemTC_TC_NVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemTC_TC_NVActionPerformed
        // TODO add your handling code here:
        String thongTin = txtMoTaTC_TC_NV.getText();
        String giaA = txtGiaA_TC_NV.getText();
        String giaB = txtGiaB_TC_NV.getText();
        if((giaA.isEmpty() && giaB.isEmpty()== false) || (giaA.isEmpty()==false && giaB.isEmpty())){
            JOptionPane.showMessageDialog(this, "Không được bỏ trống 1 trong 2 giá thú cưng!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        ArrayList <ThuCung> ds = null;
        if(thongTin.isEmpty() && giaA.isEmpty() == false){
            if(kiemTraTimThuCung(giaA, giaB)){
                ds = ThuCungDAO.timKiemThuCungTheoGia(Integer.valueOf(giaA), Integer.valueOf(giaB));
            }
        }
        else if(giaA.isEmpty()){
            ds = ThuCungDAO.timKiemThuCung(thongTin);

        }
        else{
            if(kiemTraTimThuCung(giaA, giaB)){
                ds = ThuCungDAO.timKiemThuCungTheoGiaVaMoTa(thongTin, Integer.valueOf(giaA), Integer.valueOf(giaB));
            }
        }

        dtm = (DefaultTableModel)tblDSTC_TC_NV.getModel();
        dtm.setRowCount(0);
        for(ThuCung ob: ds){
            Vector<Object> vec = new Vector<Object>();
            vec.add(ob.getMaThuCung());
            vec.add(ob.getMaLoai());
            vec.add(ob.getMaGiong());
            vec.add(ob.getDonGia());
            vec.add(ob.getSoLuong());
            dtm.addRow(vec);
        }
        tblDSTC_TC_NV.setModel(dtm);
    }//GEN-LAST:event_btnTimKiemTC_TC_NVActionPerformed

