    private boolean kiemTraLuuThuCung(String maTC, String maLoai, String maGiong, String donGia, String soLuong){
        if(maTC.isEmpty()){
            JOptionPane.showMessageDialog(this, "Mã thú cưng không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkMaTC(maTC) == false){
            JOptionPane.showMessageDialog(this, "Mã thú cưng tối đa 20 ký tự, theo định dạng TC đứng trước dãy số, không chứa từ có dấu, ký tự đặc biệt và khoảng trắng!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(maLoai.isEmpty()){
            JOptionPane.showMessageDialog(this, "Mã loại không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(LoaiDAO.kiemTraLoai(maLoai)==false){
            JOptionPane.showMessageDialog(this, "Mã loại không tồn tại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(maGiong.isEmpty()){
            JOptionPane.showMessageDialog(this, "Mã giống không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(GiongDAO.kiemTraGiong(maGiong)==false){
            JOptionPane.showMessageDialog(this, "Mã giống không tồn tại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(donGia.isEmpty()){
            JOptionPane.showMessageDialog(this, "Đơn giá không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkSoNguyenDuong(donGia) == false){
            JOptionPane.showMessageDialog(this, "Đơn giá phải là số nguyên dương!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(soLuong.isEmpty()){
            JOptionPane.showMessageDialog(this, "Số lượng không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkSoNguyenDuong(soLuong) == false){
            JOptionPane.showMessageDialog(this, "Số lượng phải là số nguyên dương!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
