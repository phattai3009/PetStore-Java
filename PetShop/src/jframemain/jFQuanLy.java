    private void btnTimKiem_NV_QLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiem_NV_QLActionPerformed
        // TODO add your handling code here:
        ArrayList <NhanVien> dsNV = NhanVienDAO.timKiemNhanVien(txtTimKiem_NV_QL.getText());
        dtm = (DefaultTableModel)tbl_DSNV_QL.getModel();
        dtm.setRowCount(0);
        for(NhanVien ob: dsNV){
            Vector<Object> vec = new Vector<Object>();
            vec.add(ob.getMaNV());
            vec.add(ob.getMaChucVu());
            vec.add(ob.getTenNV());
            vec.add(ob.getGioiTinh());
            vec.add(ob.getNgaySinh());
            vec.add(ob.getDiaChi());
            vec.add(ob.getSdt());
            vec.add(ob.getLuong());
            vec.add(ob.getCmnd());
            vec.add(ob.getNgayVL());
            
            dtm.addRow(vec);
        }
        tbl_DSNV_QL.setModel(dtm);
    }//GEN-LAST:event_btnTimKiem_NV_QLActionPerformed

