    private void btnThongKe_TK_QLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThongKe_TK_QLActionPerformed
        // TODO add your handling code here:
        if(jDCFrom_TK_QL.getDate() == null){
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(jDCTo_TK_QL.getDate() == null){
            JOptionPane.showMessageDialog(this, "Ngày kết thúc không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String from = sdf.format(jDCFrom_TK_QL.getDate());
        String to = sdf.format(jDCTo_TK_QL.getDate());
        if(kiemTraThongKe(from, to)){
            sdf = new SimpleDateFormat("yyyy-MM-dd");
            from = sdf.format(jDCFrom_TK_QL.getDate());
            to = sdf.format(jDCTo_TK_QL.getDate());
            int tongHD = HoaDonDAO.thongKeHoaDon(from, to);
            int tongTC = HoaDonDAO.thongKeThuCung(from, to);
            long tongTien = HoaDonDAO.thongKeTongTien(from, to);
            txtTongHoaDon_TK_QL.setText(String.valueOf(tongHD));
            txtTongThuCung_TK_QL.setText(String.valueOf(tongTC));
            txtTongDoanhThu_TK_QL.setText(String.valueOf(tongTien));
        }
    }//GEN-LAST:event_btnThongKe_TK_QLActionPerformed

    private boolean  kiemTraThongKe(String from, String to){
        if(Check.checkNgayThangJDC(from) == false){
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải theo định dạng dd/MM/yyyy", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(Check.checkNgayThangJDC(to) == false){
            JOptionPane.showMessageDialog(this, "Ngày kết thúc phải theo định dạng dd/MM/yyyy", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
